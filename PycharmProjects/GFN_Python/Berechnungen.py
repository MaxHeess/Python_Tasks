#Berechnungen in Python
#Addition
a= 1+5
print(a)
#Subtraktion
a= 5-1
print(a)
#Multiplikation
a= 1*5
print(a)
#Division
a= 10/2
print(a)
#Potenzieren
a= 5**2  # oft 5^2 geschrieben, aber funktioniert nicht in Python
print(a)
#Modulo = Berechnung des Restes
a= 10%3 # gesprochen 10 modulo 3 = Ergebnis ist 1 => 10/3 = 3 Rest 1
print(a)
#floor = Berechnung der Teiler ohne das der Rest relevant ist
a= 10//3 # ergibt bei der Division vom 10/3 die Anzahl der Teilungen ohne Rest, hier also 3
print(a)